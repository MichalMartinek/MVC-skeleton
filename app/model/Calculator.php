class Calculator {
    public function sum($n1, $n2)
    {
        return $n1 + $n2;
    }
    public function difference($n1, $n2)
    {
        return $n1 - $n2;
    }
    public function product($n1, $n2)
    {
        return $n1 * $n2;
    }
}
